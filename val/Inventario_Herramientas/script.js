// Utilidades 
function confirmacion(texto) {
  return new Promise((resolve) => {
    $modalTexto.textContent = texto;
    $modal.style.display = 'block';
    const cerrar = () => ($modal.style.display = 'none');

    const onNo = () => { cleanup(); resolve(false); };
    const onSi = () => { cleanup(); resolve(true); };

    function cleanup() {
      cerrar();
      $btnNo.removeEventListener('click', onNo);
      $btnSi.removeEventListener('click', onSi);
    }

    $btnNo.addEventListener('click', onNo);
    $btnSi.addEventListener('click', onSi);
  });
}

function toast(msg, tipo = 'ok') {
  $toast.className = `toast ${tipo}`;
  $toast.textContent = msg;
  $toast.style.display = 'block';
  setTimeout(() => { $toast.style.display = 'none'; }, 2500);
}

// Render de la lista (izquierda)
function renderLista() {
  const q = $busqueda.value.trim().toLowerCase();
  const f = $filtro.value;

  $lista.innerHTML = '';

  herramientas
    .filter(h => {
      const coincideTexto =
        h.nombre.toLowerCase().includes(q) ||
        h.codigo.toLowerCase().includes(q) ||
        (h.descripcion || '').toLowerCase().includes(q);
      const coincideEstado = (f === 'todos') ? true : (h.estado === f);
      return coincideTexto && coincideEstado;
    })
    .forEach(h => {
      const div = document.createElement('div');
      div.className = 'card-herramienta';
      div.innerHTML = `
        <p><strong>${h.nombre}</strong> (${h.codigo})</p>
        <p><strong>Estado:</strong> ${h.estado}</p>
        <p>Fecha de ingreso: ${h.fechaIngreso}</p>
        <p>Fecha de salida: ${h.fechaSalida || '---'}</p>
        <p>Cantidad: ${h.cantidad}</p>
      `;
      $lista.appendChild(div);
    });
}

